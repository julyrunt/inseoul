$(function() {
	/* --------------------------------------------------------------------------------------------------------------
	 * 페이징 버튼
	 * -------------------------------------------------------------------------------------------------------------- */
	$(document).on("click", ".paging-buttons button", function() {
		var url = $(this).attr("href");
		$(location).attr("href", url);
	})
	/* --------------------------------------------------------------------------------------------------------------
	 * 검색 버튼
	 * -------------------------------------------------------------------------------------------------------------- */
	$(document).on("click", "#submit", function() {
		$("form").submit();
	})
	/* --------------------------------------------------------------------------------------------------------------
	 * 글쓰기 버튼
	 * -------------------------------------------------------------------------------------------------------------- */
	$(document).on("click", "#write", function() {
		var url = "write.do";
		$(location).attr("href", url);
	})
})